 <h3>Who can become an IBOEHS Authorized Training Provider (ATP)? </h3>
<p>
Training and educational institutions having critical systems, procedures, and arrangements to deliver training programs can apply to become an authorized training provider of IBOEHS. Training centers with ISO 9001:2015 certification or equal quality systems in place will be more eligible to become authorized training providers. Training centers must be maintaining a proven track record and good standing. </p>
<h3>Prerequisite </h3>
<p>
Training centers looking for Authorised training provider status must have an adequate number of training and administrative staff members.  
The Lead Tutor/Tutor center head must have a Registered Safety Professional (RSP) qualification from IBOEHS or an International Skill Diploma in construction Safety management(Level 6 ) from IBOSD- USA. </p>
<p>

II) Lead tutor must have a minimum of five years of industry/ training experience</p>
<p>

III) The tutor Must have a minimum of two years of industry/training experience</p>
<p>

Note:- </p>

<p>

a) All tutors must Qualify IBOEHS Train the trainer program before commencing their training delivery </p>
<p>

 b) Graduate safety professionals having five years of Industry/Training experience but not having RSP qualifications can also apply. However, they have to complete RSP qualification by paying a nominal additional fee of USD 250/- in addition to the standard accreditation fee of USD 2500/- </p>
<h3>Categories of IBOEHS Authorized training providers</h3> 
<p>
Based on financial performance, IBOEHS authorized training providers are classified as follows </p>
<p>

1.	Red - (Class I) </p>
<p>

2.	Green - (Class II) </p>
<p>

3.	Purple - (Class III) </p>
<h3>Center classification Criteria:- </h3>
<p>
a)Centers paying USD 5000/- or more to IBOEHS as a Certification fee continuously for three months will be listed as RED (Class I) authorized training providers. </p>
<p>b) To provide EHS audit service, A RED (Class I) authorized training provider's lead tutor must have an ISO 9001:2015, ISO 14001:2015 & ISO 45001:2018 certification from ACS-GP USA. </p>
 
<p>
c)Centers paying USD 3000/ to USD 4999/- to IBOEHS as a Certification fee continuously for three months will be considered GREEN (Class II)authorized training providers. </p>
<p>
d)Centers paying up to USD 3000/ to IBOEHS as a Certification fee continuously for three months will be considered PURPLE (Class III) authorized training providers. </p>
<h3>Benefits of classification;- </h3>
<p>
A 'Red' (Class I) Authorized training provider will be eligible to deliver all approved certificates, diplomas training programs, and IBOEHS-EHS onsite audit service to their clients. </p>
<p>
A 'Red'(Class I)authorized training provider will be eligible for Fifteen percent(10%) off on the fee payable to IBOEHS </p>
<p>
A 'Green'(Class II) Authorised training provider will deliver all approved certificates, diplomas training programs, except IBOEHS-EHS onsite audit service to their clients. </p>
<p>
A 'GREEN'(Class II)authorized training provider will be eligible for Five percent(5%) off on the fee payable to IBOEHS </p>
<p>

A 'PURPLE'(Class III) authorized training provider will not be eligible for any fee waiver from the amount payable to IBOEHS </p>
<h3>How to apply for accreditation:- </h3>
<p>
Centers looking for accreditation may please write to Info@iboehs.org. 
Centers must submit a completed corporate verification form along with asked documents for further process. </p>
<h3>Accreditation fee:- </h3>
<p>
The accreditation fee includes </p>
<p>

a)	Center audit fee </p>
<p>


b)	Center accreditation processing fee</p>
<p>

c)	Train the trainer course fee for a lead tutor/one trainer </p>
<p>

Note:- </p>
<p>
a) To accredit additional trainers, the Center must pay an additional USD 500/- towards training the trainer program for each trainer. </p>
<p>
b) Centers not having RSP qualified tutors must pay an additional USD 500/- towards the RSP course fee. </p>
<h3>The accreditation process</h3>
<p>
The International Board of environmental health and safety follows a four-step process for center accreditation. </p>
<p>
Step 1. On receipt of the completed corporate verification form and associated documents, IBOEHS shall raise an invoice towards the accreditation fee for USD 2500/-</p>
<p>
Step 2. On receipt of the accreditation fee, the IBOEHS auditor shall carry out a physical/virtual audit of the Center within 07 working days of the receipt of the accreditation fee</p>
<p>
Step 3. In case of audit objection, IBOEHS will grant an abatement period of 15 days, and a follow-up audit will be carried out after 15 days. However, USD 2000/- shall be refunded to the applicant in case of a permanent rejection. </p>
<p>
Step 4. A draft copy of the MOU will be exchanged, and a final accreditation certificate will be issued on receipt of all required documents.</p>

